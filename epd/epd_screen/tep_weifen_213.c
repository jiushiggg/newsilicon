static void lcd_chkstatus(void)
{
  
#define WAIT_COUNT	10	//10s超时
  SetWathcDog();
  UINT8 i=WAIT_COUNT;
  
  smclk_timer_isr(FALSE, 0,0); 
  Delay_ms(30);
  while(!(isEPD_W21_BUSY) && (0 != i) ) //低表示忙
  {
    sys_wakeup_wait(200); 
    i--;
  }
  
  smclk_timer_isr(FALSE, 0,0); 
  if(i == 0)
    while(1);
  
}

void EPD_IC_Rst(void)
{
  EPD_W21_RST_1;
  Delay_us(300);
  EPD_W21_RST_0;		
  Delay_us(300);
  EPD_W21_RST_1;
  Delay_us(300);
}

static UINT8 display_data_reversal(UINT8 temp1)
{
  UINT8 retvalue =0,i=0;
  retvalue |= (temp1 &1);
  
  for(i=1;i<8;i++)
  {
    retvalue <<= 1;
    retvalue |=(((1<<i) & temp1) >>i);
  }
  
  return retvalue;
}
