void over_screen(void)
{
  EPD_W21_WriteCMD(0x22);
  EPD_W21_WriteDATA(0xC7);		//Load LUT from MCU(0x32), Display update
  EPD_W21_WriteCMD(0x20);
  Delay_ms(10);
  if(hlt_busy_1()!= TRUE)
    gerr_info = TR3_SCREEN_ERR1;
}

