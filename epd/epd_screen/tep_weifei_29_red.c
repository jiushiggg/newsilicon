static void lcd_chkstatus(void)
{
  
#define WAIT_COUNT	10	//10s
  SetWathcDog();
  UINT8 i=WAIT_COUNT;
  
  smclk_timer_isr(FALSE, 0,0); 
  Delay_ms(30);
  while(!(isEPD_W21_BUSY) && (0 != i) ) //
  {
    sys_wakeup_wait(200); 
    i--;
  }
  
  smclk_timer_isr(FALSE, 0,0); 
  if(i == 0)
    while(1);
  
}


