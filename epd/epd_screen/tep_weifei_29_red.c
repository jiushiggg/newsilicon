void EPD_W21_Init(void){
  
  epd_spi_init();
  EPD_IC_Rst();
  EPD_IC_Rst();
  
  EPD_W21_WriteCMD(0x06);         //boost soft start
  EPD_W21_WriteDATA (0x17);		//A
  EPD_W21_WriteDATA (0x17);		//B
  EPD_W21_WriteDATA (0x17);		//C       
  
  EPD_W21_WriteCMD(0x04);  
  lcd_chkstatus();
  
#ifdef EPD_NEW_ENCLOSURE 
  EPD_W21_WriteCMD(0x00);			//panel setting
  EPD_W21_WriteDATA(0x03);		//KW-BF   KWR-AF	BWROTP 0f	BWOTP 1f
  EPD_W21_WriteDATA(0x0b);
  
#else
  EPD_W21_WriteCMD(0x00);			//panel setting
  EPD_W21_WriteDATA(0x0f);		//KW-BF   KWR-AF	BWROTP 0f	BWROTP 1f
  EPD_W21_WriteDATA(0x0b);
#endif
  
  
  EPD_W21_WriteCMD(0x61);			//resolution setting
  EPD_W21_WriteDATA (0x80);        	 
  EPD_W21_WriteDATA (0x01);		
  EPD_W21_WriteDATA (0x28);
  
  EPD_W21_WriteCMD(0X50);			//VCOM AND DATA INTERVAL SETTING			
  EPD_W21_WriteDATA(0x77);		//VBDF 17|D7 VBDW 97 VBDB 57		VBDF F7 VBDW 77 VBDB 37  VBDR B7  
}

void screen_close(void)
{
  EPD_W21_WriteCMD(0X50);
  EPD_W21_WriteDATA(0xf7);	
  EPD_W21_WriteCMD(0X02);  	//power off
  lcd_chkstatus();
  EPD_W21_WriteCMD(0X07);  	//deep sleep
  EPD_W21_WriteDATA(0xA5);
  close_epd();
}
BOOL screen_busy_fun(void)
{
#define TIMES_2S_BUSY 60                //2min
  if(epd_scn_tp.busy_times == TIMES_2S_BUSY)
  {
    while(1);
  }
  else if((isEPD_W21_BUSY))         //
  {
    epd_scn_tp.busy_times=0;
    epd_scn_tp.screen_busy_flag = FALSE;
    screen_close();
    return FALSE;
  }
  else
  {
    epd_scn_tp.screen_busy_flag = TRUE;
    return TRUE;
  }
}
