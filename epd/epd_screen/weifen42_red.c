static void EPD_W21_WriteCMD(unsigned char command)
{
  //	while(isEPD_W21_BUSY == 0);	// wait	
  SPI_Delay(2);
  if(ic_switch==0x00)
  {
    EPD_W21_CS_0; 
    EPD_W21_CS1_1;
  }
  else if(ic_switch==0x55)
  {	
    EPD_W21_CS1_0;
    EPD_W21_CS_1;
  }
  else if(ic_switch==0xFF)
  {	
    EPD_W21_CS_0;
    EPD_W21_CS1_0;
  }       
  else
  {	SPI_Delay(1);
  }
  SPI_Delay(2);             
  EPD_W21_DC_0;		// command write
  SPI_Write(command);
  EPD_W21_CS_1;
  EPD_W21_CS1_1;
  SPI_Delay(2); 
}
static void EPD_W21_WriteDATA(unsigned char command)
{
  
  SPI_Delay(2);
  if(ic_switch==0x00)
  {EPD_W21_CS_0;
  EPD_W21_CS1_1;
  }
  else if(ic_switch==0x55)
  {	EPD_W21_CS1_0;
  EPD_W21_CS_1;
  }
  else if(ic_switch==0xFF)
  {	EPD_W21_CS_0;
  EPD_W21_CS1_0;
  }       
  else
  {SPI_Delay(1);
  }
  SPI_Delay(2);                  
  EPD_W21_DC_1;		// command write
  SPI_Write(command);
  EPD_W21_CS_1;
  EPD_W21_CS1_1;
  SPI_Delay(2); 	  
}
static void send_2byte(UINT8 val) {
  int i;
  union map{
    UINT16 B;
    struct {
      UINT8 L;
      UINT8 H;
    };
  }D = {
    .B = 0,
  };
  
  for (i = 7; i >= 0; i--) {
    if (val & (1 << i))
      D.B |= (3 << (i *2));
  }
  
  SPI_Write(D.H);
  SPI_Write(D.L);
}
void EPD_W21_WriteDATA_led(UINT8 command)
{
  
  SPI_Delay(2);
  if(ic_switch==0x00)
  {EPD_W21_CS_0;
  EPD_W21_CS1_1;
  }
  else if(ic_switch==0x55)
  {	EPD_W21_CS1_0;
  EPD_W21_CS_1;
  }
  else if(ic_switch==0xFF)
  {	EPD_W21_CS_0;
  EPD_W21_CS1_0;
  }       
  else
  {SPI_Delay(1);
  }
  SPI_Delay(2);                  
  EPD_W21_DC_1;		// command write
  send_2byte(command);
  EPD_W21_CS_1;
  EPD_W21_CS1_1;
  SPI_Delay(2); 
  
}

static void lcd_chkstatus(void)
{
  
#define WAIT_COUNT	40	//80s
  SetWathcDog();
  UINT8 i=WAIT_COUNT;
  
  SetWathcDog();  
  
  do
  {
    sys_wakeup_wait(400); 
    i--;
  }
  while(!(isEPD_W21_BUSY) && (0 != i) ); 
  SetWathcDog();  
  if(i == 0)
    while(1);
  
}


void display_lcd(file_id_t fd)
{
  
  unsigned int i,j;
   UINT8  *data = TEMP.BYTE64;
  for(j=0;j<300;j++)	
  {
    f_read(fd, (299-j) * 50 ,data, 25); 
    for(i=0;i<25;i++)
    {      
      ic_switch=0x00;  
      if(fd ==F_BMP_RED)
        EPD_W21_WriteDATA(data[i]);  
      else
        EPD_W21_WriteDATA_led(data[i]);      
    }
    f_read(fd, (299-j) * 50+25 ,data, 25); 
    for(i=25;i<50;i++)
    {            
      ic_switch=0x55;	
      if(fd ==F_BMP_RED)
        EPD_W21_WriteDATA(data[i-25]);  
      else    
        EPD_W21_WriteDATA_led(data[i-25]);  
    }   
    Delay_ms(2);
    ic_switch=0xFF;
  }
}
