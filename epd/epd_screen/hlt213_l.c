void clear_screen(void)
{
  WRITE_LUT();
  EPD_W21_WriteCMD(0x4E);		// set RAM x address count to 0;
  EPD_W21_WriteDATA(0x00);
  EPD_W21_WriteCMD(0x4F);		// set RAM y address count to 250;	2D13
  EPD_W21_WriteDATA(0x00);
  
  
  EPD_W21_WriteCMD(0x24);
}
