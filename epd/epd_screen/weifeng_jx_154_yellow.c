static UINT8 display_data_reversal(UINT8 temp1)
{
  UINT8 retvalue =0,i=0;
  retvalue |= (temp1 &1);
  
  for(i=1;i<8;i++)
  {
    retvalue <<= 1;
    retvalue |=(((1<<i) & temp1) >>i);
  }
  
  return retvalue;
}

