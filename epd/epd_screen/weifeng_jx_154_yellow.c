void EPD_W21_Init(void){
  
  epd_spi_init();
  
  EPD_W21_BS_0;		// 4 wire spi mode selected
  EPD_W21_RST_0;		// Module reset
  Delay_ms(1000);
  EPD_W21_RST_1;
  Delay_ms(1000);
  
  EPD_W21_WriteCMD(0x01); 
  EPD_W21_WriteDATA (0x03);	    //设置高低电压
  EPD_W21_WriteDATA (0x00);
  EPD_W21_WriteDATA (0x2b);
  EPD_W21_WriteDATA (0x2b);
  EPD_W21_WriteDATA (0x0a);
  EPD_W21_WriteCMD(0x06);         //boost设定
  EPD_W21_WriteDATA (0x17);
  EPD_W21_WriteDATA (0x17);
  EPD_W21_WriteDATA (0x17);
  EPD_W21_WriteCMD(0x04);        
  EPD_W21_WriteCMD(0xE3);       //上电
  EPD_W21_WriteDATA(0xf0); 
  lcd_chkstatus();                //??????  
  
  EPD_W21_WriteCMD(0X00);
  EPD_W21_WriteDATA(0xcf);		//选择最大分辨率
  EPD_W21_WriteDATA(0x0d);
  
  EPD_W21_WriteCMD(0X50);
  EPD_W21_WriteDATA(0x77);
  EPD_W21_WriteCMD(0x30);			//PLL设定
  EPD_W21_WriteDATA (0x3a);        
  
  EPD_W21_WriteCMD(0X60);
  EPD_W21_WriteDATA(0x22);
  EPD_W21_WriteCMD(0x61);			//像素设定
  EPD_W21_WriteDATA (0x98);       //200像素	 
  EPD_W21_WriteDATA (0x00);		//300像素 
  EPD_W21_WriteDATA (0x98);
  
  EPD_W21_WriteCMD(0x82);			//vcom设定
  EPD_W21_WriteDATA (0x12);	
  
}
void screen_close(void)
{
  EPD_W21_WriteCMD(0X50);
  EPD_W21_WriteDATA(0xf7);	
  EPD_W21_WriteCMD(0X02);  	//power off
  lcd_chkstatus();
  EPD_W21_WriteCMD(0X07);  	//deep sleep
  EPD_W21_WriteDATA(0xA5);
  close_epd();
}

BOOL screen_busy_fun(void)
{
#define TIMES_2S_BUSY 60                //2min
  if(epd_scn_tp.busy_times == TIMES_2S_BUSY)
  {
    while(1);
  }
  else if((isEPD_W21_BUSY))         //高不忙
  {
    epd_scn_tp.busy_times=0;
    epd_scn_tp.screen_busy_flag = FALSE;
    screen_close();
    return FALSE;
  }
  else
  {
    epd_scn_tp.screen_busy_flag = TRUE;
    return TRUE;
  }
}
