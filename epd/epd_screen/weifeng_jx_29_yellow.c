  Delay_ms(100);
  EPD_W21_RST_1;
  Delay_ms(100);
  SetWathcDog();
  
  EPD_W21_WriteCMD(0x01);        	//Set VGH VGL   VSH VSL
  EPD_W21_WriteDATA (0x03);	    
  EPD_W21_WriteDATA (0x00);
  EPD_W21_WriteDATA (0x2b);
  EPD_W21_WriteDATA (0x2b);
  EPD_W21_WriteDATA (0x09);
 
